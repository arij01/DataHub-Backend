import { Component, OnInit } from '@angular/core';
import { Chart } from 'chart.js';
import { HttpClient } from '@angular/common/http';

@Component({
  selector: 'app-graph',
  templateUrl: './graph.component.html',
  styleUrls: ['./graph.component.css']
})
export class GraphComponent implements OnInit {
  chartData: any;

  constructor(private http: HttpClient) {}

 
  ngOnInit() {
    this.initializePolarAreaChart();
}



initializePolarAreaChart() {
  const polarAreaChartElement = document.querySelector('#polarAreaChart');
  
  if (polarAreaChartElement instanceof HTMLCanvasElement) {
    new Chart(polarAreaChartElement, {
      type: 'polarArea',
      data: {
        labels: [
          'Eating',
          'Drinking',
          'Sleeping',
          'Designing',
          'Coding',
          'Cycling',
          'Running'
        ],
        datasets: [{
          label: 'Polar Area Chart',
          data: [65, 59, 90, 81, 56, 55, 40],
          backgroundColor: [
            'rgba(255, 99, 132, 0.5)',
            'rgba(54, 162, 235, 0.5)',
            'rgba(255, 206, 86, 0.5)',
            'rgba(75, 192, 192, 0.5)',
            'rgba(153, 102, 255, 0.5)',
            'rgba(255, 159, 64, 0.5)',
            'rgba(255, 99, 132, 0.5)'
          ],
          borderColor: [
            'rgba(255, 99, 132, 1)',
            'rgba(54, 162, 235, 1)',
            'rgba(255, 206, 86, 1)',
            'rgba(75, 192, 192, 1)',
            'rgba(153, 102, 255, 1)',
            'rgba(255, 159, 64, 1)',
            'rgba(255, 99, 132, 1)'
          ],
          borderWidth: 1
        }]
      }
    });
  } else {
    console.error('Element with id "polarAreaChart" not found or not an HTMLCanvasElement.');
  }
}

}
